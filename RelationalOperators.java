public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = 5;
        System.out.println(a == b);
        System.out.println(b == c);
        System.out.println(b != c);
        System.out.println(b > c);
        System.out.println(a < c);
        System.out.println(a >= b);
        System.out.println(b <= c);
    }
}
