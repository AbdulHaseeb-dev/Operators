public class LogicalOperators {
    public static void main(String[] args) {

        // Logical AND (&&)
        System.out.println((3>2) && (5>3));

        // Logical OR (||)
        System.out.println((3<2) || (5>3));

        // Logical NOT (!)
        System.out.println(!(3>2));
        System.out.println(!(5<8));
    }
}
